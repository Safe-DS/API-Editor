export const lessThan = function (a: number, b: number): boolean {
    return a < b;
};

export const lessThanOrEqual = function (a: number, b: number): boolean {
    return a <= b;
};

export const equals = function (a: number, b: number): boolean {
    return a === b;
};

export const greaterThanOrEqual = function (a: number, b: number): boolean {
    return a >= b;
};

export const greaterThan = function (a: number, b: number): boolean {
    return a > b;
};
